int main(int argc, char** argv) 
{

	aes_ui_t _aes_ui;

	aes_ui_init();
	aes_host_lib_init();
	aes_engine_init();

	// extract arguments
	if ( AES_UI_SUCCESS == aes_ui(argc, argv, &_aes_ui) )
	{		
		// process in engine
		aes_engine(&_aes_ui);
	}

    return 0;
}

